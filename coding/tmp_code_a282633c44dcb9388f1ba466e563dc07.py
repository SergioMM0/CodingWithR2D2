def subtract(num1, num2):
    """
    This function subtracts num2 from num1.
    
    Args:
        num1 (float): The first number.
        num2 (float): The second number.
        
    Returns:
        float: The result of the subtraction.
    """
    return num1 - num2

# Test the function
print(subtract(10, 3))  # Output: 7
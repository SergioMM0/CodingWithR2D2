def add_numbers(a, b):
    """
    This function adds two numbers together.
    
    Parameters:
    a (float): The first number to be added.
    b (float): The second number to be added.
    
    Returns:
    float: The sum of the two input numbers.
    """
    return a + b

# Test the function
print(add_numbers(5, 7))  # Output: 12